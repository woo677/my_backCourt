package com.back.court.dto;

public class CourtDTO {
	private int courtIdx;
	private String courtName;
	private String courtAddress;
	private String courtInfo;
	private int courtPrice;
	private String courtState;
	private String courtOfficial;
	private String firstImageName;
	private int jjim;

	public int getCourtIdx() {
		return courtIdx;
	}

	public void setCourtIdx(int courtIdx) {
		this.courtIdx = courtIdx;
	}

	public String getCourtName() {
		return courtName;
	}

	public void setCourtName(String courtName) {
		this.courtName = courtName;
	}

	public String getCourtAddress() {
		return courtAddress;
	}

	public void setCourtAddress(String courtAddress) {
		this.courtAddress = courtAddress;
	}

	public String getCourtInfo() {
		return courtInfo;
	}

	public void setCourtInfo(String courtInfo) {
		this.courtInfo = courtInfo;
	}

	public int getCourtPrice() {
		return courtPrice;
	}

	public void setCourtPrice(int courtPrice) {
		this.courtPrice = courtPrice;
	}

	public String getCourtState() {
		return courtState;
	}

	public void setCourtState(String courtState) {
		this.courtState = courtState;
	}

	public String getCourtOfficial() {
		return courtOfficial;
	}

	public void setCourtOfficial(String courtOfficial) {
		this.courtOfficial = courtOfficial;
	}

	public int getJjim() {
		return jjim;
	}

	public void setJjim(int jjim) {
		this.jjim = jjim;
	}

	public String getFirstImageName() {
		return firstImageName;
	}

	public void setFirstImageName(String firstImageName) {
		this.firstImageName = firstImageName;
	}

}
