package com.back.admin.dto;

public class AdminReportDTO {
	private int reportIdx;
	private String reportState;
	private String reportSubject;
	private String reportId;
	private String reportDate;
	private String reportAdminId;
	private String reportContent;
	private String reportFeed;
	private String reportCategory;
	private String reportCategoryIdx;

	public int getReportIdx() {
		return reportIdx;
	}

	public void setReportIdx(int reportIdx) {
		this.reportIdx = reportIdx;
	}

	public String getReportState() {
		return reportState;
	}

	public void setReportState(String reportState) {
		this.reportState = reportState;
	}

	public String getReportSubject() {
		return reportSubject;
	}

	public void setReportSubject(String reportSubject) {
		this.reportSubject = reportSubject;
	}

	public String getReportId() {
		return reportId;
	}

	public void setReportId(String reportId) {
		this.reportId = reportId;
	}

	public String getReportDate() {
		return reportDate;
	}

	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}

	public String getReportAdminId() {
		return reportAdminId;
	}

	public void setReportAdminId(String reportAdminId) {
		this.reportAdminId = reportAdminId;
	}

	public String getReportContent() {
		return reportContent;
	}

	public void setReportContent(String reportContent) {
		this.reportContent = reportContent;
	}

	public String getReportFeed() {
		return reportFeed;
	}

	public void setReportFeed(String reportFeed) {
		this.reportFeed = reportFeed;
	}

	public String getReportCategory() {
		return reportCategory;
	}

	public void setReportCategory(String reportCategory) {
		this.reportCategory = reportCategory;
	}

	public String getReportCategoryIdx() {
		return reportCategoryIdx;
	}

	public void setReportCategoryIdx(String reportCategoryIdx) {
		this.reportCategoryIdx = reportCategoryIdx;
	}
}
